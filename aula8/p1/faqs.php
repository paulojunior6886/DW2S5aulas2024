    <h1>FAQ sobre PHP</h1>
    <p>Aqui você encontrará respostas para as perguntas mais frequentes sobre PHP.</p>
    <h2>O que é PHP?</h2>
    <p>PHP é uma linguagem de script de uso geral, de código aberto e amplamente utilizada no mercado, especialmente
        para desenvolvimento web. Ela pode ser incorporada ao HTML, permitindo a criação de páginas dinâmicas e
        interativas.</p>
        <img src="php2.png" alt="Logotipo do PHP em um fundo azul." width="250" height="200">
    <h2>Para que serve o PHP?</h2>
    <p>O PHP pode ser usado para uma variedade de tarefas, como:</p>
    <ul>
        <li>Criar sites dinâmicos e interativos;</li>
        <li>Desenvolver aplicações web complexas, como sistemas de e-commerce, sistemas de gerenciamento de conteúdo
            (CMS) e muito mais;</li>
        <li>Processar dados de formulários, arquivos e bancos de dados;</li>
        <li>Interagir com APIs de serviços externos, como redes sociais e plataformas de pagamento.</li>
    </ul>
    <h2>Quais são as vantagens de usar PHP?</h2>
    <ul>
        <li>Fácil de aprender</li>
        <li>Código aberto</li>
        <li>Versátil</li>
        <li>Amplamente utilizado</li>
    </ul>
    <h2>Quais são as desvantagens de usar PHP?</h2>
    <ul>
        <li>Segurança: O PHP pode ser vulnerável a ataques de segurança se não for usado de forma adequada. É importante
            seguir boas práticas de desenvolvimento para mitigar esses riscos.</li>
        <li>Desempenho: O PHP pode ser mais lento que outras linguagens de programação em alguns casos. No entanto,
            existem técnicas de otimização que podem ajudar a melhorar o desempenho de aplicações PHP.</li>
        <li>Falta de recursos modernos: Enquanto o PHP está sempre evoluindo, algumas linguagens mais recentes podem ter
            recursos mais modernos integrados. A escolha da linguagem certa depende dos requisitos específicos do
            projeto.</li>
    </ul>
